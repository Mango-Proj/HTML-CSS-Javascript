// ============================================================
// var.js — var 키워드의 특징과 문제점
//
// var는 자바스크립트의 초창기(ES1~ES5)부터 있던 변수 선언 방식입니다.
// 현재는 let/const 사용이 권장되지만,
// 오래된 코드나 레거시 프로젝트에서 자주 마주치므로 반드시 이해해야 합니다.
// ============================================================

console.log('===== 1. 중복 선언 허용 =====');

// var는 같은 이름으로 여러 번 선언해도 에러가 발생하지 않습니다.
// 이것은 대규모 코드에서 실수를 유발하는 큰 문제점입니다.

var userName = 'John';
console.log(userName); // 'John'

var userName = 'Doe'; // 같은 변수명으로 다시 선언 → 에러 없이 덮어씀
console.log(userName); // 'Doe' (기존 값이 의도치 않게 바뀜!)

// 문제 시나리오:
// 수백 줄짜리 코드에서 위쪽에서 선언한 변수명을 잊고 아래에서 또 선언하면,
// 의도하지 않게 값이 바뀌어도 에러가 나지 않아 디버깅이 매우 어렵습니다.


console.log('\n===== 2. 함수 스코프 (Function Scope) =====');

// var는 함수 스코프입니다.
// → 함수 내에서 선언한 var는 함수 밖에서 접근 불가
// → 함수 안의 if/for 블록({})은 var에게 아무 의미가 없음

function greet() {
  var message = 'Hello!'; // 함수 내부에서 선언
  console.log(message);   // 'Hello!' — 함수 안에서는 접근 가능
}
greet();
// console.log(message); // ReferenceError: message is not defined
//                        → 함수 밖에서는 접근 불가 (함수 스코프)


console.log('\n===== 3. 블록 스코프 없음 (블록 탈출 문제) =====');

// if, for, while 같은 {} 블록 안에서 var로 선언하면
// 블록을 탈출해서 밖에서도 접근이 됩니다.

if (true) {
  var age = 20; // {} 블록 안에서 선언했지만...
  console.log('블록 안 age:', age); // 20
}
console.log('블록 밖 age:', age); // 20 — 블록 밖에서도 접근됨! (의도치 않은 동작)

// for 문도 마찬가지입니다
for (var i = 0; i < 3; i++) {
  // 반복문 내부에서 선언한 i
}
console.log('for문 밖 i:', i); // 3 — 반복문 밖에서도 i가 살아있음!

// 이 문제 때문에 for 문에서 var i 를 사용하면
// 다른 곳의 i 변수를 의도치 않게 덮어쓸 수 있습니다.


console.log('\n===== 4. var 호이스팅 (Hoisting) =====');

// 호이스팅: 자바스크립트 엔진이 코드 실행 전에
// var 선언을 스코프 최상단으로 끌어올리는 현상.
// 단, 선언만 끌어올려지고 초기화(값 할당)는 제자리에 남습니다.

// 아래 코드는 실제로 이렇게 동작합니다:
// var score; ← 선언이 자동으로 최상단으로 이동 (호이스팅)
// console.log(score); // undefined ← 선언은 됐지만 아직 값이 없음
// score = 100; ← 이 줄에서 비로소 초기화

console.log('호이스팅 전 score:', score); // undefined (에러 아님!)
var score = 100;
console.log('호이스팅 후 score:', score); // 100

// 결론: var는 선언 전에 사용해도 에러가 안 나지만 undefined가 나옵니다.
// 이것은 버그를 숨기는 원인이 됩니다.


console.log('\n===== 5. var를 쓰면 안 되는 이유 요약 =====');

// 문제 1: 중복 선언 → 실수해도 에러 없음
// 문제 2: 블록 탈출 → if/for 안에서 선언해도 밖에서 보임
// 문제 3: 호이스팅 → 선언 전에 접근해도 에러 없이 undefined 반환
//
// 이 세 가지 문제로 인해 예측하기 어려운 버그가 생깁니다.
// → 현대 자바스크립트에서는 var 대신 let / const 를 사용합니다.
